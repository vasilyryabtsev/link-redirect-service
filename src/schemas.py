from pydantic import BaseModel

class Message(BaseModel):
    """
    Модель для стандартизированного ответа с сообщением.
    
    Используется для возврата простых текстовых сообщений от API,
    например, при успешном выполнении операции или ошибке.

    Атрибуты:
        message: Текст сообщения, которое требуется передать клиенту
    """
    message: str
